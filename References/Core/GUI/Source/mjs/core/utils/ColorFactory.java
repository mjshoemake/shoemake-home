// Java imports
import java.awt.Color;

/**
 * This is the Witness color factory.  This should be used instead of the
 * specifying specific colors defined in the java.awt.Color class.  Colors
 * can be added or replaced by extending this class for the developer's needs.
 * @author   Mike Shoemake
 * @version  1.0
 * @date     12/20/2002
 */
public class ColorFactory
{
   /**
    * Black.
    */
   public static Color black = Color.black;

   /**
    * Blue.
    */
   public static Color blue = Color.blue;

   /**
    * Bright Gray.
    */
   public static Color brightGray = new Color(-2830136);

   /**
    * Cyan.
    */
   public static Color cyan = Color.cyan;

   /**
    * Dark Gray.
    */
   public static Color darkGray = Color.darkGray;

   /**
    * Gray.
    */
   public static Color gray = Color.gray;

   /**
    * Green.
    */
   public static Color green = Color.green;

   /**
    * Light Gray.
    */
   public static Color lightGray = Color.lightGray;

   /**
    * Magenta.
    */
   public static Color magenta = Color.magenta;

   /**
    * Orange.
    */
   public static Color orange = Color.orange;

   /**
    * Pink.
    */
   public static Color pink = Color.pink;

   /**
    * Red.
    */
   public static Color red = Color.red;

   /**
    * White.
    */
   public static Color white = Color.white;

   /**
    * Yellow.
    */
   public static Color yellow = Color.yellow;
}
