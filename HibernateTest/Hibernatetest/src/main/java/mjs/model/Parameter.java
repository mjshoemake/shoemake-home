package mjs.model;

import lombok.Data;

/**
 * This is an object representation of a name-value pair
 * for a parameter list.
 * 
 * @author mshoemake
 */
@SuppressWarnings("unused")
public @Data class Parameter
{
   /**
    * The parameter name.
    */
   private final String name = null;

   /**
    * The parameter value.
    */
   private final Object value = null;

}
